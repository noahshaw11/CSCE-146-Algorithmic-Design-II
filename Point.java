
public class Point {
	private double x,y;
	public Point(double aX, double aY)
	{
		this.setX(aX);
		this.setY(aY);
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	

}
